#include <stdio.h>
/**
 * add - addition
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substract
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int mod(int a, int b)
{
	return (a % b);
}
